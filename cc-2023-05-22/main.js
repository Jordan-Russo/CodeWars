// get ascii value of character

// given a character as a string
// return the ASCII value it has
// Examples:
  //  getASCII('!') => 33
  //  getASCII('A') => 65
  //  getASCII(' ') => 32

// Get the ASCII value of the first charatcter in the string
// Return the ASCII value number

const getASCII = char => char.charCodeAt(0)